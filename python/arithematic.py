val1 = 2
val2 = 3

## using the addition operator +
res = val1 + val2
print(res)

## using the substraction operator -
sub = val1 - val2
print(sub)

##using the multiplication operator *
mul = val2 * val1
print(mul)

## using the division operator /

div = round(val2/val1) # round to a whole number
print(div)

## using the modulus Operator %

mod = val2 % val1
print(mod)

## using the exponential operator **
exp = val1**val2
print(exp)

## using floor divisor //
floor1 = val1//val2 # it return 0 2//3
